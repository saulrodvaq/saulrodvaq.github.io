                        let observer = new IntersectionObserver((entries, observer) => {
                            entries.forEach(entry => {
                                if (entry.isIntersecting) {
                                    const countElements = entry.target.querySelectorAll('.count');

                                    countElements.forEach(element => {
                                        const from = parseInt(element.getAttribute(
                                            'data-from'));
                                        const to = parseInt(element.getAttribute('data-to'));
                                        const duration = 2000; // Duración en milisegundos
                                        const increment = Math.ceil((to - from) / (duration /
                                            10)); // Incremento por paso de 10ms

                                        let current = from;
                                        const interval = setInterval(() => {
                                            current += increment;
                                            if (current >= to) {
                                                element.textContent = to;
                                                clearInterval(interval);
                                            } else {
                                                element.textContent = current;
                                            }
                                        }, 10);
                                    });
                                    observer.unobserve(entry
                                        .target); // Deja de observar después de que se inicie el conteo
                                }
                            });
                        }, {
                            threshold: 0.1
                        }); // Iniciar la cuenta cuando al menos el 10% del elemento esté en la vista

                        observer.observe(document.querySelector(
                            '#stats-section')); // Observar el elemento con id 'stats-section'
